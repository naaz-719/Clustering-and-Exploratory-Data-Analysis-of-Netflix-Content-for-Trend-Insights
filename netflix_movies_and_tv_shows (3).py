# Hypothesis 1: Chi-Square Test of Independence (Categorical vs Categorical)
# Prepare contingency table: content type counts by country (top 5 countries for clarity)
top_countries = data['country'].value_counts().nlargest(5).index
subset = data[data['country'].isin(top_countries)]
contingency_table = pd.crosstab(subset['country'], subset['type'])

# Perform Chi-Square Test
chi2, p_value, dof, expected = chi2_contingency(contingency_table)

print("Hypothesis 1 - Chi-Square Test")
print(f"Chi2 Statistic: {chi2:.4f}")
print(f"P-value: {p_value:.4f}")

if p_value < 0.05:
    print("Reject Null Hypothesis: Content type distribution depends on country.\n")
else:
    print("Fail to Reject Null Hypothesis: Content type distribution is independent of country.\n")

#Hypothesis 2: Independent T-Test (numerical vs categorical)
# Hypothesis: The average movie duration is significantly different between the USA and India.

