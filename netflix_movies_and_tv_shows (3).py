"""# **Step 7 : Textual Data Preprocessing and TF-IDF Vectorization**"""

# Initialize stopwords and lemmatizer
import string
stop_words = set(stopwords.words('english'))
lemmatizer = WordNetLemmatizer()

# Function to clean text
def clean_text(text):
    if pd.isnull(text):
        return ""
    text = text.lower()  # lowercase
    text = text.translate(str.maketrans('', '', string.punctuation))  # remove punctuation
    tokens = text.split()
    tokens = [word for word in tokens if word not in stop_words]  # remove stopwords
    tokens = [lemmatizer.lemmatize(word) for word in tokens]  # lemmatization
    return " ".join(tokens)

# Apply text cleaning to the 'description' column
data['cleaned_description'] = data['description'].apply(clean_text)

# TF-IDF Vectorization
tfidf_vectorizer = TfidfVectorizer(max_features=5000)  # limit features for performance
tfidf_matrix = tfidf_vectorizer.fit_transform(data['cleaned_description'])

# tfidf_matrix is now a numeric feature matrix ready for clustering

"""# **Step 8 :Clustering Modeling: KMeans and Agglomerative Clustering with Evaluation**"""

# Step 1: Reduce the dimensionality of the TF-IDF matrix using TruncatedSVD
# This technique is ideal for sparse text data.
from sklearn.decomposition import TruncatedSVD

svd = TruncatedSVD(n_components=100, random_state=42)
tfidf_reduced = svd.fit_transform(tfidf_matrix)

# Step 2: Systematically evaluate a range of 'k' values to find the best-performing clusters
K_range = range(2, 11)
best_k = -1
best_silhouette = -1
best_kmeans_model = None

print("Evaluating KMeans for optimal number of clusters...")
for k in K_range:
    kmeans_model = KMeans(n_clusters=k, random_state=42, n_init=10)
    kmeans_labels = kmeans_model.fit_predict(tfidf_reduced)
    sil_score = silhouette_score(tfidf_reduced, kmeans_labels)

    print(f"k={k}: Silhouette Score = {sil_score:.4f}")

    if sil_score > best_silhouette:
        best_silhouette = sil_score
        best_k = k
        best_kmeans_model = kmeans_model

